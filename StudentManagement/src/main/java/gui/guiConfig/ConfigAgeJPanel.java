    private boolean HasConfigAge(Set<Cauhinh> cauhinhs) {
        int check = 0;
        for (dto.Cauhinh next : cauhinhs) {
            switch (next.getLoaiThuocTinh()) {
                case "minAgeToClass":
                    check++;
                    break;
                case "maxAgeToClass":
                    check++;
                    break;
            }
        }
        return check == 2;
    }

